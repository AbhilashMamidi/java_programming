class Combinational_op 
{
	public static void main(String[] args) 
	{
		int a = 5;
		a+=2;
	System.out.println(a);
	
	int b = 7;
	b-=3;
	System.out.println(b);

	int c = 8;
	c*=2;
	System.out.println(c);

	int d = 47;
	d/=5;
	System.out.println(d);

	int e = 47;
		e%=5;
	System.out.println(e);
	}
}
