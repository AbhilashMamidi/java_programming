class Logical_operators
{
	public static void main(String[] args) 
	{
		System.out.println(5>4&&5<4);
		System.out.println(4!=4&&10==10);
		System.out.println(20>10&&20==20);
		System.out.println(35<34&&5<6);

		System.out.println(4==4||5>6);
		System.out.println(10<9||5<6);
		System.out.println(11<12||10==10);
		System.out.println(20>21||20<19);

		System.out.println(!(5==5));
		System.out.println(!(5>=6));


	}
}
