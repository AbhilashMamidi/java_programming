class Pattern 
{
	public static void main(String[] args) 
	{
		for (int a=1;a<=6 ;a++ )
		{
			for (int b=1;b<=6 ;b++ )
			{
				System.out.print(" " + '*' + " " );
			}
			System.out.println();
		}
		for (int c=1;c<=6 ;c++ )
		{
			for (int d=1;d<=6 ;d++ )
			{
				System.out.print(" " + '*' + " " );
			}
			System.out.println();
		}
	}
}
